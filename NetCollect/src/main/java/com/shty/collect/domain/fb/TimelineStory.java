package com.shty.collect.domain.fb;

public class TimelineStory {
	private Long id;
	private Long commentcount;
	private String content;
	private int imageCodeListSize;
	private Long likecount;
	private String pubdate;
	private String storyId;
	private String title;
	private Long FBTarget_id;
	private String videourl;
	private int videostatus;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getCommentcount() {
		return commentcount;
	}
	public void setCommentcount(Long commentcount) {
		this.commentcount = commentcount;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getImageCodeListSize() {
		return imageCodeListSize;
	}
	public void setImageCodeListSize(int imageCodeListSize) {
		this.imageCodeListSize = imageCodeListSize;
	}
	public Long getLikecount() {
		return likecount;
	}
	public void setLikecount(Long likecount) {
		this.likecount = likecount;
	}
	public String getPubdate() {
		return pubdate;
	}
	public void setPubdate(String pubdate) {
		this.pubdate = pubdate;
	}
	public String getStoryId() {
		return storyId;
	}
	public void setStoryId(String storyId) {
		this.storyId = storyId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Long getFBTarget_id() {
		return FBTarget_id;
	}
	public void setFBTarget_id(Long fBTarget_id) {
		FBTarget_id = fBTarget_id;
	}
	public String getVideourl() {
		return videourl;
	}
	public void setVideourl(String videourl) {
		this.videourl = videourl;
	}
	public int getVideostatus() {
		return videostatus;
	}
	public void setVideostatus(int videostatus) {
		this.videostatus = videostatus;
	}
	
	
}
