package com.shty.collect.domain.twitter;

public class Search {
private String id;
private String searchUserId;
private String searchUserName;
private String searchUserNickName;
private String searchUserURL;
private String searchUserImgUrl;
private String searchKeyWord;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getSearchUserId() {
	return searchUserId;
}
public void setSearchUserId(String searchUserId) {
	this.searchUserId = searchUserId;
}
public String getSearchUserName() {
	return searchUserName;
}
public void setSearchUserName(String searchUserName) {
	this.searchUserName = searchUserName;
}
public String getSearchUserNickName() {
	return searchUserNickName;
}
public void setSearchUserNickName(String searchUserNickName) {
	this.searchUserNickName = searchUserNickName;
}
public String getSearchUserURL() {
	return searchUserURL;
}
public void setSearchUserURL(String searchUserURL) {
	this.searchUserURL = searchUserURL;
}
public String getSearchUserImgUrl() {
	return searchUserImgUrl;
}
public void setSearchUserImgUrl(String searchUserImgUrl) {
	this.searchUserImgUrl = searchUserImgUrl;
}
public String getSearchKeyWord() {
	return searchKeyWord;
}
public void setSearchKeyWord(String searchKeyWord) {
	this.searchKeyWord = searchKeyWord;
}

}
