package com.shty.collect.domain.twitter;

public class TwitterProfile {
	private String ID;
	private String UserCardName;
	private String UserNickName;
	private String UserCardBio;
	private String UserCardUrl;
	private String UserCardJoinDateDesc;
	private String UserCardLocation;
	private String UserAvatarName;
	private Long TargetID;
	private String UserAvatarUrl;

	public String getID() {
		return ID;
	}

	public void setID(String ID) {
		this.ID = ID;
	}

	public String getUserCardName() {
		return UserCardName;
	}

	public void setUserCardName(String userCardName) {
		UserCardName = userCardName;
	}

	public String getUserNickName() {
		return UserNickName;
	}

	public void setUserNickName(String userNickName) {
		UserNickName = userNickName;
	}

	public String getUserCardBio() {
		return UserCardBio;
	}

	public void setUserCardBio(String userCardBio) {
		UserCardBio = userCardBio;
	}

	public String getUserCardUrl() {
		return UserCardUrl;
	}

	public void setUserCardUrl(String userCardUrl) {
		UserCardUrl = userCardUrl;
	}

	public String getUserCardJoinDateDesc() {
		return UserCardJoinDateDesc;
	}

	public void setUserCardJoinDateDesc(String userCardJoinDateDesc) {
		UserCardJoinDateDesc = userCardJoinDateDesc;
	}

	public String getUserCardLocation() {
		return UserCardLocation;
	}

	public void setUserCardLocation(String userCardLocation) {
		UserCardLocation = userCardLocation;
	}

	public String getUserAvatarName() {
		return UserAvatarName;
	}

	public void setUserAvatarName(String userAvatarName) {
		UserAvatarName = userAvatarName;
	}

	public Long getTargetID() {
		return TargetID;
	}

	public void setTargetID(Long targetID) {
		TargetID = targetID;
	}

	public String getUserAvatarUrl() {
		return UserAvatarUrl;
	}

	public void setUserAvatarUrl(String userAvatarUrl) {
		UserAvatarUrl = userAvatarUrl;
	}
	

}
